n = int(input('Digite um numero: '))
d = n * 2 # Dobro
t = n * 3 # Triplo
r = n **(1/2) # Raiz Quadrada

print('O dobro de {} é: {} '.format(n,d))
print('O triplo de {} é: {} '.format(n,t))
print('A raiz quadrada de {} é: {:.4f} '.format(n,r)) 


